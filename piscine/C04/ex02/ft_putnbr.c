#include <unistd.h>

void	ft_putnbr(int nb);

void	ft_putchar(char c);

int	main(void)
{
	int	nbr;

	nbr = -45;
	ft_putnbr(nbr);
	return (0);
}


void	ft_putnbr(int nb)
{
	if (nb == -2147483648)
	{
		write(1, "-2147483648", 11);
	}
	else if (nb < 0)
	{
		ft_putchar('-');
		nb = -nb;
		ft_putnbr(nb);
	}
	else if (nb > 9)
	{
		ft_putnbr(nb / 10);
		ft_putnbr(nb % 10);
	}
	else
		ft_putchar(nb + '0');
}

void	ft_putchar(char c)
{
	write (1, &c, 1);
}
